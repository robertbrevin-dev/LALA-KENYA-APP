# ── STEP 2: Wire up pages ─────────────────────────────────────────────────────
IMPORT_LINE = "import { useLanguage } from '../context/LanguageContext.tsx';"
HOOK_LINE = "  const { t } = useLanguage();"

def add_lang_hook(path, replacements):
    c, crlf = read(path)

    # Add import if missing
    if "useLanguage" not in c:
        # Insert after last import line
        lines = c.split("\n")
        last_import = 0
        for idx, l in enumerate(lines):
            if l.startswith("import "):
                last_import = idx
        lines.insert(last_import + 1, IMPORT_LINE)
        c = "\n".join(lines)

    # Add hook if missing
    if "const { t } = useLanguage()" not in c:
        # Insert after the first const/let/useState inside the component
        lines = c.split("\n")
        for idx, l in enumerate(lines):
            if "const {" in l or "useState" in l:
                lines.insert(idx, HOOK_LINE)
                break
        c = "\n".join(lines)

    # Apply replacements
    for old, new in replacements.items():
        c = c.replace(old, new)

    write(path, c, crlf)
    print(f"Fixed: {path}")

